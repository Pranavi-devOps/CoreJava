class CompareNumbers {

    public static void main(String[] args) {

        int a = 25;
        int b = 15;

        if (a > b) {
            System.out.println("Larger number: " + a);
            System.out.println("Smaller number: " + b);
        } else {
            System.out.println("Larger number: " + b);
            System.out.println("Smaller number: " + a);
        }
    }
}
